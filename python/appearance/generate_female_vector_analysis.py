def confidence_for(image_id: str, score: int, primary: str) -> tuple[float, float, bool]:
    n = int(image_id[2:])
    base = 0.70 if n <= 20 else 0.66
    if primary in {"스타일리시/화려형", "성숙/분위기형"}:
        base -= 0.02
    if score >= 86:
        base -= 0.02
    quality = min(0.78, base + 0.04)
    heavy_filter = score >= 78 or n >= 21
    return round(base, 2), round(quality, 2), heavy_filter


def axis_stats(evaluations: list[dict]) -> dict[str, dict[str, float]]:
    out = {}
    for axis in AXES:
        values = sorted(e["appearance_vector"][axis] for e in evaluations)
        mean = sum(values) / len(values)
        variance = sum((v - mean) ** 2 for v in values) / len(values)
        out[axis] = {
            "mean": round(mean, 3),
            "std": round(math.sqrt(variance), 3),
            "min": round(values[0], 2),
            "p25": round(values[len(values) // 4], 2),
            "median": round(values[len(values) // 2], 2),
            "p75": round(values[(len(values) * 3) // 4], 2),
            "max": round(values[-1], 2),
            "spread": round(values[-1] - values[0], 2),
        }
    return out


