def target_type_for(image_id: str) -> str:
    n = int(image_id[2:])
    for start, end, target_type in TARGET_BY_RANGE:
        if start <= n <= end:
            return target_type
    raise ValueError(image_id)


def blend_vector(primary: str, secondary: list[str], overrides: dict[str, float]) -> dict[str, float]:
    weights = [(primary, 0.72)]
    if len(secondary) >= 1 and secondary[0] in TYPE_PROFILES:
        weights.append((secondary[0], 0.18))
    if len(secondary) >= 2 and secondary[1] in TYPE_PROFILES:
        weights.append((secondary[1], 0.10))

    total = sum(weight for _, weight in weights)
    vector = {}
    for axis in AXES:
        value = sum(TYPE_PROFILES[k][axis] * weight for k, weight in weights) / total
        vector[axis] = round(value, 2)

    for axis, value in overrides.items():
        if axis in vector:
            vector[axis] = round(value, 2)

    # 부드러운인상 + 날카로운인상은 보통 0.9~1.1 사이가 되게 보정한다.
    total_edge = vector["부드러운인상"] + vector["날카로운인상"]
    if total_edge < 0.88:
        vector["부드러운인상"] = round(vector["부드러운인상"] + (0.88 - total_edge), 2)
    elif total_edge > 1.12:
        vector["날카로운인상"] = round(max(0.0, vector["날카로운인상"] - (total_edge - 1.12)), 2)
    return vector


